/** Per-ratio purpose-built layout notes — not crop/stretch instructions. */
export function buildRatioCompositions(centralVisual) {
  return {
    '1:1':
      `Balanced square: headline in upper-left third; central visual — ${centralVisual}; generous negative space; CTA/support band along lower edge.`,
    '4:5':
      `Mobile-feed-first: extra vertical breathing room; headline top third separated from central visual; ${centralVisual}; critical content above bottom UI overlap zone.`,
    '9:16':
      `Stories/Reels safe zones: no critical text in top 14% or bottom 20%; vertical workflow progression — ${centralVisual}; large readable headline in safe middle band; never center-crop the square.`,
    '1.91:1':
      `Horizontal: headline left half, visual right half; ${centralVisual}; minimal text; readable at small link-ad sizes; fill the wide frame.`,
  };
}

function waveConcept({
  number,
  name,
  audience,
  language = 'en',
  headline,
  supportingLine,
  benefits,
  colorFamilyId,
  animationTemplate,
  testingTag,
  sourceHypothesis,
  recommendedAudience,
  recommendedPriority,
  visualDirection,
  centralVisual,
  visualStyle = 'operations',
  claimIds = ['dedicated-ft'],
  trustBadge = 'No unapproved seals — overlay in design tool only.',
  offer = 'No published offer — direct to staffing conversation.',
  productionStatus = 'Ready for design',
  groupLabel,
  remotionNotes = '',
  capcutNotes = '',
  cta = 'Learn More',
}) {
  const ratioCompositions = buildRatioCompositions(centralVisual);
  const talentDirection =
    visualStyle === 'portrait'
      ? 'Professional virtual medical administrator — credible, warm, framed on right third. Wardrobe matches color family.'
      : visualStyle === 'workflow-human'
        ? 'No portrait. Human-centered workflow graphics only — phone, calendar, inbox, follow-up cards with warm polished treatment.'
        : `Clean healthcare operations graphics — ${centralVisual}. No random stock doctors. Not software/SaaS UI. Generous negative space. Be Vietnam typography overlaid later.`;

  return {
    number,
    name,
    role: 'Virtual Medical Admin',
    audience,
    language,
    headline,
    supportingLine,
    benefits,
    talentDirection,
    colorFamilyId,
    offer,
    trustBadge,
    cta,
    staticFormats: ALL_RATIOS,
    videoDuration: '15s',
    animationTemplate,
    formId: 'form-a',
    claimIds,
    productionStatus,
    group: testingTag === 'Adjacent Test' ? 8 : 7,
    groupLabel,
    reviewer: '',
    performanceNotes: '',
    testingTag,
    sourceHypothesis,
    recommendedAudience,
    recommendedPriority,
    visualDirection,
    visualStyle,
    ratioCompositions,
    remotionNotes,
    capcutNotes,
  };
}

